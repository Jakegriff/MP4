package mp3;

public class Landmarks {
	
	private String name;
	private int location;
	
	public String getName() {
		return(name);
	}
	
	public int getLocation() {
		return(location);
	}
	
	public void setName(String newName) {
		name = newName;
	}
	
	public void setLoc(int newLoc) {
		location = newLoc;
	}

}
